// Helper functions
function updateElement(selector, key, lang, keepHtml = false) {
    const element = document.querySelector(selector);
    if (element && translations[lang][key]) {
        if (keepHtml) {
            const span = element.querySelector('span');
            if (span) {
                element.innerHTML = `${span.outerHTML} ${translations[lang][key]}`;
            } else {
                element.textContent = translations[lang][key];
            }
        } else {
            element.textContent = translations[lang][key];
        }
    }
}

function updateAllElements(selector, keys, lang) {
    const elements = document.querySelectorAll(selector);
    elements.forEach((element, index) => {
        if (index < keys.length && translations[lang][keys[index]]) {
            element.textContent = translations[lang][keys[index]];
        }
    });
}

function updateWorkCard(selector, lang, fields) {
    const card = document.querySelector(selector);
    if (card) {
        if (fields.title) updateElement(`${selector} .project-title`, fields.title, lang);
        if (fields.desc) updateElement(`${selector} .project-description`, fields.desc, lang);
        if (fields.date) updateElement(`${selector} .project-date`, fields.date, lang);
    }
}

function updateOtherCard(selector, lang, fields) {
    const card = document.querySelector(selector);
    if (card) {
        if (fields.title) updateElement(`${selector} h3`, fields.title, lang);
        if (fields.subtitle) updateElement(`${selector} h5, ${selector} h7`, fields.subtitle, lang);
        if (fields.desc) updateElement(`${selector} p`, fields.desc, lang);
        if (fields.date) updateElement(`${selector} .date`, fields.date, lang);
    }
}